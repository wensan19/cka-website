const detailRoot = document.body.dataset.detailRoot || "";
const detailId = document.body.dataset.detailId;
const detail = detailPages[detailId];
const detailImage = document.querySelector(".detail-carousel-image");
const detailDots = document.querySelector(".detail-carousel-dots");
const detailPrev = document.querySelector(".detail-carousel-prev");
const detailNext = document.querySelector(".detail-carousel-next");
const detailMeta = document.querySelector(".detail-meta");
let activeDetailSlide = 0;
let detailTimer = null;
let detailTransitionTimer = null;
let detailLightbox = null;
let detailLightboxContent = null;

function createDetailLightbox() {
  if (detailLightbox) return;

  detailLightbox = document.createElement("div");
  detailLightbox.className = "detail-media-lightbox";
  detailLightbox.hidden = true;
  detailLightbox.setAttribute("aria-hidden", "true");

  const closeButton = document.createElement("button");
  closeButton.className = "detail-media-lightbox-close";
  closeButton.type = "button";
  closeButton.setAttribute("aria-label", "Close enlarged media");
  closeButton.innerHTML = "&times;";

  detailLightboxContent = document.createElement("div");
  detailLightboxContent.className = "detail-media-lightbox-content";

  detailLightbox.append(closeButton, detailLightboxContent);
  document.body.appendChild(detailLightbox);

  closeButton.addEventListener("click", closeDetailLightbox);
  detailLightbox.addEventListener("click", (event) => {
    if (event.target === detailLightbox) {
      closeDetailLightbox();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeDetailLightbox();
    }
  });
}

function openDetailLightbox() {
  if (!detail || !detailImage || !detail.images.length) return;

  createDetailLightbox();
  detailLightboxContent.innerHTML = "";

  const image = detail.images[activeDetailSlide];
  const enlargedImage = document.createElement("img");
  enlargedImage.src = detailRoot + image.src;
  enlargedImage.alt = image.alt;
  enlargedImage.loading = "eager";

  detailLightboxContent.appendChild(enlargedImage);
  detailLightbox.hidden = false;
  detailLightbox.setAttribute("aria-hidden", "false");
}

function closeDetailLightbox() {
  if (!detailLightbox || !detailLightboxContent) return;

  detailLightbox.hidden = true;
  detailLightbox.setAttribute("aria-hidden", "true");
  detailLightboxContent.innerHTML = "";
}

function setDetailSlide(index, animate = true) {
  if (!detail || !detailImage || detail.images.length === 0) return;

  activeDetailSlide = (index + detail.images.length) % detail.images.length;
  const image = detail.images[activeDetailSlide];

  clearTimeout(detailTransitionTimer);
  if (animate) {
    detailImage.classList.add("is-changing");
    detailTransitionTimer = window.setTimeout(() => {
      detailImage.src = detailRoot + image.src;
      detailImage.alt = image.alt;
      detailImage.classList.remove("is-changing");
    }, 220);
  } else {
    detailImage.src = detailRoot + image.src;
    detailImage.alt = image.alt;
    detailImage.classList.remove("is-changing");
  }

  detailDots?.querySelectorAll("button").forEach((dot, dotIndex) => {
    dot.classList.toggle("active", dotIndex === activeDetailSlide);
    dot.setAttribute(
      "aria-current",
      dotIndex === activeDetailSlide ? "true" : "false",
    );
  });
}

function startDetailCarousel() {
  clearInterval(detailTimer);
  if (!detail || detail.images.length < 2) return;
  detailTimer = window.setInterval(
    () => setDetailSlide(activeDetailSlide + 1),
    5200,
  );
}

function pauseDetailCarousel() {
  clearInterval(detailTimer);
}

if (detail && detailImage && detailDots) {
  document.title = detail.title + " | CKA Design";
  document.querySelector(".detail-title").textContent = detail.title;
  document.querySelector(".detail-category").textContent = detail.category;
  document.querySelector(".detail-type").textContent = detail.type;
  document.querySelector(".detail-copy h2").textContent = detail.title;
  document.querySelector(".detail-description p").textContent =
    detail.description;

  detailMeta.innerHTML = "";
  detail.details.forEach((item) => {
    const wrapper = document.createElement("div");
    const term = document.createElement("dt");
    const description = document.createElement("dd");
    term.textContent = item.label;
    description.textContent = item.value;
    wrapper.append(term, description);
    detailMeta.appendChild(wrapper);
  });

  detail.images.forEach((image, index) => {
    const dot = document.createElement("button");
    dot.type = "button";
    dot.setAttribute("aria-label", "Show " + image.alt);
    dot.addEventListener("click", () => {
      setDetailSlide(index);
      startDetailCarousel();
    });
    detailDots.appendChild(dot);
  });

  detailPrev?.addEventListener("click", () => {
    setDetailSlide(activeDetailSlide - 1);
    startDetailCarousel();
  });

  detailNext?.addEventListener("click", () => {
    setDetailSlide(activeDetailSlide + 1);
    startDetailCarousel();
  });

  const frame = document.querySelector(".detail-carousel-frame");
  frame?.addEventListener("mouseenter", pauseDetailCarousel);
  frame?.addEventListener("mouseleave", startDetailCarousel);
  frame?.addEventListener("focusin", pauseDetailCarousel);
  frame?.addEventListener("focusout", startDetailCarousel);
  frame?.addEventListener("click", (event) => {
    if (event.target.closest("button")) return;
    openDetailLightbox();
  });

  setDetailSlide(0, false);
  startDetailCarousel();
}
